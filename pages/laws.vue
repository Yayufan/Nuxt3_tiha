                        law）。施行此種法律的國家有西班牙、拉脫維亞、奧地利，比利時、法國、意大利、芬蘭、匈牙利、丹麥、波蘭、瑞典、以色列、盧森堡、斯洛伐克、希臘、保加利亞、新加坡等國，一般而言，採用Opt-out
